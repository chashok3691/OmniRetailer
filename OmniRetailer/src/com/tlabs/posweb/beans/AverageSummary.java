package com.tlabs.posweb.beans;

public class AverageSummary {

	private String averageStockValue;
	private String averageTax;
	private String averageInclusiveTax;
	private String averageExclusiveTax;
	private String averageStock;
	
	public String getAverageStockValue() {
		return averageStockValue;
	}
	public void setAverageStockValue(String averageStockValue) {
		this.averageStockValue = averageStockValue;
	}
	public String getAverageTax() {
		return averageTax;
	}
	public void setAverageTax(String averageTax) {
		this.averageTax = averageTax;
	}
	public String getAverageInclusiveTax() {
		return averageInclusiveTax;
	}
	public void setAverageInclusiveTax(String averageInclusiveTax) {
		this.averageInclusiveTax = averageInclusiveTax;
	}
	public String getAverageExclusiveTax() {
		return averageExclusiveTax;
	}
	public void setAverageExclusiveTax(String averageExclusiveTax) {
		this.averageExclusiveTax = averageExclusiveTax;
	}
	public String getAverageStock() {
		return averageStock;
	}
	public void setAverageStock(String averageStock) {
		this.averageStock = averageStock;
	}
	
}
