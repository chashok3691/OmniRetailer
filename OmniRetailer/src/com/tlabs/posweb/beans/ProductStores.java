package com.tlabs.posweb.beans;

public class ProductStores {
	private String store_id = "";
	private String store_desc = "";
	private String latest_comments = "";

public String getStore_id() {
	return store_id;
}
public void setStore_id(String store_id) {
	this.store_id = store_id;
}

public String getLatest_comments() {
	return latest_comments;
}
public void setLatest_comments(String latest_comments) {
	this.latest_comments = latest_comments;
}
public String getStore_desc() {
	return store_desc;
}
public void setStore_desc(String store_desc) {
	this.store_desc = store_desc;
}

}
