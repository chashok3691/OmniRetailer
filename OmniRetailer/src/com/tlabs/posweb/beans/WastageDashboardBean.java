package com.tlabs.posweb.beans;

public class WastageDashboardBean {
	private String perishedStockAmount;
	private String searchCriteria;
	private String damagedStockAmount;
	private String expiredStockAmount;
	private String theftStockAmount;
	private String totalWastageAmount;
	
	
	public String getPerishedStockAmount() {
		return perishedStockAmount;
	}
	public void setPerishedStockAmount(String perishedStockAmount) {
		this.perishedStockAmount = perishedStockAmount;
	}
	public String getSearchCriteria() {
		return searchCriteria;
	}
	public void setSearchCriteria(String searchCriteria) {
		this.searchCriteria = searchCriteria;
	}
	public String getDamagedStockAmount() {
		return damagedStockAmount;
	}
	public void setDamagedStockAmount(String damagedStockAmount) {
		this.damagedStockAmount = damagedStockAmount;
	}
	public String getExpiredStockAmount() {
		return expiredStockAmount;
	}
	public void setExpiredStockAmount(String expiredStockAmount) {
		this.expiredStockAmount = expiredStockAmount;
	}
	public String getTheftStockAmount() {
		return theftStockAmount;
	}
	public void setTheftStockAmount(String theftStockAmount) {
		this.theftStockAmount = theftStockAmount;
	}
	public String getTotalWastageAmount() {
		return totalWastageAmount;
	}
	public void setTotalWastageAmount(String totalWastageAmount) {
		this.totalWastageAmount = totalWastageAmount;
	}
	
	
}
